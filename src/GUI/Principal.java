    public void setcombos(){
        for (int i = 0; i < combos.size(); i++) {
            DefaultComboBoxModel model = (DefaultComboBoxModel) combos.get(i).getModel();
            model.removeAllElements();
            
        }
        for (int i = 0; i < combos.size(); i++) {
            for (int j = 0; j < filaseleccionadas; j++) {
                combos.get(i).addItem(""+(j+1));
                
            }
            
            
        }
    }
