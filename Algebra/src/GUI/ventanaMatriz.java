    private void mostrar(String[] txt){     
        for (int i = 0; i < txt.length; i++) {         
            for (int j = 0; j <componenteslbl.size(); j++) { 
                
                if (componenteslbl.get(j).getName().equals(txt[i])){
                                    
                    componenteslbl.get(j).setVisible(true);    
                }
                
            }
        }
        for (int i = 0; i < txt.length; i++) {
            for (int j = 0; j < componentestxt1.size(); j++) {             
                    if (componentestxt1.get(j).getName().equals(txt[i])){
                    componentestxt1.get(j).setVisible(true);
                    componentestxt2.get(j).setVisible(true);
                        
                }  
            }
        }
        this.revalidate();
        this.repaint();
        
        
    }
    private void agregarMatriz(String[] txt){
        if (matrizCorrecta(txt)){
            ArrayList<Double> arraytmp = new ArrayList<Double>();
            for (int i = 0; i < txt.length; i++) {
                if (txt[i].equals("-")){  
                    principal.getMatrizPrincipal().aadirFila(new ArrayList<>(arraytmp));            
                    arraytmp.clear();
                }
                else{
                        double num1 = 0;
                        double num2 = 0;
                        double result = 0;
                        num1 = Double.parseDouble(componentestxt1.get(Integer.parseInt(txt[i])).getText());                      
                        num2 = Double.parseDouble(componentestxt2.get(Integer.parseInt(txt[i])).getText());
                        result = num1/num2;
                        arraytmp.add(result);                            
                }
            
            
            }
        }
        else{
            JOptionPane.showMessageDialog(null,
                    "La matriz que se ingreso se ecuentra incorrecta ,recuerde que no puede poseeer ceros,ni letras , ni encontrarse vacia");
        
        }
        
        
    }
    
